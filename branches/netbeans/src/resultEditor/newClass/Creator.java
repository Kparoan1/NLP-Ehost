    private void saveNewAnnotatedClassname(String newAnnotatedClass){
        if(( newAnnotatedClass == null)||(newAnnotatedClass.length() < 1 ))
            return ;
        resultEditor.annotationClasses.Depot classdepot = new resultEditor.annotationClasses.Depot();
        classdepot.addElement(newAnnotatedClass.trim(), "User_Built_in_eHOST", this.classcolor, true, false);
        config.project.ProjectConf projectconf = new config.project.ProjectConf(
                    env.Parameters.WorkSpace.CurrentProject);
        projectconf.saveConfigure();

        resultEditor.spanEdit.AnnotationClassChooser ac = new resultEditor.spanEdit.AnnotationClassChooser();
        ac.applyJustBuiltAnnotatedClass( newAnnotatedClass.trim(), null );
    }

