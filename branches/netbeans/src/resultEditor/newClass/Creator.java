    /**while user is typing, check repetitive or not */
    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        jLabel2.setText(null);
        jLabel2.setVisible(false);

        

        String typed = jTextField1.getText().trim() + evt.getKeyChar();

        //System.out.println("keys = [" + typed + "]");

        if (( jTextField1.getText() != null )&&( typed.trim().length() > 0 )){
            if ( repetitiveCheck( typed.trim() )) {
                jButton_apply.setEnabled(false);
                jLabel2.setText(" Annotated classs \"" + typed.trim()  + "\" has existed!!!");
                jLabel2.setIcon( jLabel3.getIcon() );
                jLabel2.setVisible(true);
                
                commons.Tools.beep();

            }else {
                jButton_apply.setEnabled(true);
            }

        }else
            jButton_apply.setEnabled(false);
    }//GEN-LAST:event_jTextField1KeyTyped

